public class ModularExamples {
    public void positiveModular() {
        System.out.println(1024 % 10);
    }

    public void negativeModular() {
        System.out.println(-1024 % 10);
    }

    public static void main(String[] args) {
        ModularExamples modularExamples = new ModularExamples();
        modularExamples.positiveModular();
        modularExamples.negativeModular();
    }
}
